    private void jButton_InsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_InsertActionPerformed
        String var = this.jComboBox_InsertAsset.getSelectedItem().toString();
        switch (var) {
            case "Tree" -> gp.Trees_in_map.add(new Tree(gp)); 
            case "Fruits" -> gp.Fruits_in_map.add(new Fruit(gp));
            case "Mushroom" -> gp.Mushrooms_in_map.add(new Mushrooms(gp));
            case "Rock" -> gp.Rocks_in_map.add(new Rocks(gp));
            case "Jaguar" -> gp.animals_in_map.add(new Jaguar(gp)); 
            case "Deer" -> gp.animals_in_map.add(new Deer(gp));
            case "Crabs" -> gp.animals_in_map.add(new Crab(gp));
            case "Snakes" -> gp.animals_in_map.add(new Snake(gp));
            case "Crocodile" -> gp.animals_in_map.add(new Crocodiles(gp));          
            default -> throw new AssertionError();
        }
    }//GEN-LAST:event_jButton_InsertActionPerformed

