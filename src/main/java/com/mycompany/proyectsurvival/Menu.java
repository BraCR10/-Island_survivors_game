    private void btn_provokeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_provokeActionPerformed
        // TODO add your handling code here:
       Object selectedItem = cbx_events.getSelectedItem();
        if (selectedItem != null) {
            String selectedEvent = selectedItem.toString();
            if(selectedEvent.equals("Character Accident"))gp.generateAccident();
            else  if(selectedEvent.equals("Mild Illness") || selectedEvent.equals("Severe Illness"))gp.createIllness(selectedEvent);
            else if(selectedEvent.equals("None") || txt_durationDays.getText().equals(""));
            else
                gp.createStrom(Integer.parseInt(txt_durationDays.getText()), selectedEvent);
            txt_durationDays.setText("");
        }
    }//GEN-LAST:event_btn_provokeActionPerformed

